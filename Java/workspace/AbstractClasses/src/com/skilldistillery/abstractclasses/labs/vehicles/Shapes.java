package com.skilldistillery.abstractclasses.labs.vehicles;

public abstract class Shapes {
	private int x;
	private int y;





	public abstract double getArea();

	public Shapes() {

	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}



}
